            STOP"**EXIT FUNCTION REACHED**"
            !WRITE(MSG_FILE,*) "**EXIT FUNCTION REACHED**"
            !CALL XIT    ! ABAQUS EXIT FUNCTION
        END SUBROUTINE

       END MODULE PARAMETER_MODULE
